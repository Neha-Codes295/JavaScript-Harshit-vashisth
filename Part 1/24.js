// break keyword
for(let i = 1; i<=6; i++){
    if(i===4){
        break;
    }
    console.log(i);
}
// break jb aata h to sidha loop se bahr ho jate h fir h


// continue keyword
for(let i = 1; i<=6; i++){
    if(i===4){
       continue;
    }
    console.log(i);
}
// continue jb aata h to sirf vo condition vala nhi chlega jo match hoga baaki sb chlenge mtbl vo condition vala skip ho jayega